import { readFile, writeFile } from 'node:fs/promises'

const contractPath = new URL('../contracts/agent-runtime-contract.json', import.meta.url)
const tsOutputPath = new URL('../src/shared/agent-runtime-contract.ts', import.meta.url)
const csOutputPath = new URL(
  '../sidecars/Ola.Native.Worker/Modules/AgentRuntime/AgentRuntimeContract.g.cs',
  import.meta.url
)

const contract = JSON.parse((await readFile(contractPath, 'utf8')).replace(/^\uFEFF/, ''))
const entries = Object.entries(contract.routes)

if (!Number.isSafeInteger(contract.version) || contract.version < 1) {
  throw new Error('Agent Runtime contract version must be a positive integer')
}
if (entries.length === 0 || contract.capabilities.length === 0) {
  throw new Error('Agent Runtime contract must define routes and capabilities')
}
if (new Set(entries.map(([, route]) => route)).size !== entries.length) {
  throw new Error('Agent Runtime contract routes must be unique')
}
if (new Set(contract.capabilities).size !== contract.capabilities.length) {
  throw new Error('Agent Runtime contract capabilities must be unique')
}

/**
 * @param {string} _match
 * @param {string} _prefix
 * @param {string} char
 * @returns {string}
 */
// eslint-disable-next-line @typescript-eslint/explicit-function-return-type
function upperCaseMatch(_match, _prefix, char) {
  return char.toUpperCase()
}

/**
 * @param {string} value
 * @returns {string}
 */
// eslint-disable-next-line @typescript-eslint/explicit-function-return-type
function pascalCase(value) {
  return value.replace(/(^|[_-])([a-z])/g, upperCaseMatch)
}

const ts = `// Generated by scripts/generate-agent-runtime-contract.mjs. Do not edit manually.\n\nexport const AGENT_RUNTIME_CONTRACT_VERSION = ${contract.version}\n\nexport const AGENT_RUNTIME_ROUTES = {\n${entries
  .map(([key, route]) => `  ${key}: '${route}'`)
  .join(
    ',\n'
  )}\n} as const\n\nexport type AgentRuntimeRoute = (typeof AGENT_RUNTIME_ROUTES)[keyof typeof AGENT_RUNTIME_ROUTES]\n\nexport const AGENT_RUNTIME_CAPABILITIES = [\n${contract.capabilities
  .map((capability) => `  '${capability}'`)
  .join(
    ',\n'
  )}\n] as const\n\nexport type AgentRuntimeCapability = (typeof AGENT_RUNTIME_CAPABILITIES)[number]\n`

const cs = `// <auto-generated />\n// Generated by scripts/generate-agent-runtime-contract.mjs. Do not edit manually.\n\ninternal static class AgentRuntimeContract\n{\n    public const int Version = ${contract.version};\n\n${entries
  .map(([key, route]) => `    public const string ${pascalCase(key)}Route = "${route}";`)
  .join(
    '\n'
  )}\n\n    public static readonly HashSet<string> Capabilities = new(StringComparer.Ordinal)\n    {\n${contract.capabilities.map((capability) => `        "${capability}",`).join('\n')}\n    };\n}\n`

if (process.argv.includes('--check')) {
  const [currentTs, currentCs] = await Promise.all([
    readFile(tsOutputPath, 'utf8'),
    readFile(csOutputPath, 'utf8')
  ])

  if (currentTs !== ts || currentCs !== cs) {
    throw new Error(
      'Generated Agent Runtime contract is out of date. Run npm run generate:agent-runtime-contract.'
    )
  }

  console.log(`Agent Runtime contract v${contract.version} is up to date`)
} else {
  await Promise.all([writeFile(tsOutputPath, ts, 'utf8'), writeFile(csOutputPath, cs, 'utf8')])

  console.log(`Generated Agent Runtime contract v${contract.version}`)
}
